// 10 nos decrement by 2(reverse no.)
# include<stdio.h>
void main()
{
	int counter;
	for (counter=10;counter>=1;counter-=2)
	{
		printf("%d\n",counter);
	}

}

//Algorithum
//1) start
//2) declare counter
//3) if counter=10, counter greater than equal to 1, counter decrement by 2
//then printf counter
//4) stop
